a = 21
b = 21

# ==
print(f"a es igual a b: {a == b}") # a es igual a b: True

# !=
print(f"a es diferente de b: {a != b}") # a es diferente de b: False

# >
print(f"a es mayor que b: {a > b}") # a es mayor que b: False

# <
print(f"a es menor que b: {a < b}") # a es menor que b: False

# >=
print(f"a es mayor o igual que b: {a >= b}") # a es mayor o igual que b: True

# <=
print(f"a es menor o igual que b: {a <= b}") # a es menor o igual que b: True