a = 21
b = 3

# Suma
c = a + b
print("Suma:", c) # Suma: 24

# Resta
c = a - b
print("Resta:", c) # Resta: 18

# Multiplicación
c = a * b
print("Multiplicación:", c) # Multiplicación: 63

# División
c = a / b
print("División:", c) # División: 7.0

# División entera
c = a // b
print("División entera:", c) # División entera: 7

# Modulo
c = a % b
print("Modulo:", c) # Modulo: 0

# Potencia
c = a ** b
print("Potencia:", c) # Potencia: 6561

